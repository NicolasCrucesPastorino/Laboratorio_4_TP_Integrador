package entidad;

public class ReporteTransferencia {
	private int TransferenciasAño;
	private int TransferenciasMes;
	private float TotalAño;
	private float TotalMes;
	private float PromedioAño;
	private float PromedioMes;
	private float MaximoAño;
	private float MaximoMes;
	
	
	public ReporteTransferencia() {
		
	}


	public int getTransferenciasAño() {
		return TransferenciasAño;
	}


	public void setTransferenciasAño(int transferenciasAño) {
		TransferenciasAño = transferenciasAño;
	}


	public int getTransferenciasMes() {
		return TransferenciasMes;
	}


	public void setTransferenciasMes(int transferenciasMes) {
		TransferenciasMes = transferenciasMes;
	}


	public float getTotalAño() {
		return TotalAño;
	}


	public void setTotalAño(float totalAño) {
		TotalAño = totalAño;
	}


	public float getTotalMes() {
		return TotalMes;
	}


	public void setTotalMes(float totalMes) {
		TotalMes = totalMes;
	}


	public float getPromedioAño() {
		return PromedioAño;
	}


	public void setPromedioAño(float promedioAño) {
		PromedioAño = promedioAño;
	}


	public float getPromedioMes() {
		return PromedioMes;
	}


	public void setPromedioMes(float promedioMes) {
		PromedioMes = promedioMes;
	}


	public float getMaximoAño() {
		return MaximoAño;
	}


	public void setMaximoAño(float maximoAño) {
		MaximoAño = maximoAño;
	}


	public float getMaximoMes() {
		return MaximoMes;
	}


	public void setMaximoMes(float maximoMes) {
		MaximoMes = maximoMes;
	}
	
	
	

}