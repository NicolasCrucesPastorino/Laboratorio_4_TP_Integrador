package entidad;

public class ReporteCuotas {
	private int mes;
	private int cuotasPagas;
	private int cuotasPendientes;
	private float totalCobrado;
	private float totalPendiente;
	private float ganancia;
	
	
	public ReporteCuotas() {
		
	}


	public int getMes() {
		return mes;
	}


	public void setMes(int mes) {
		this.mes = mes;
	}


	public int getCuotasPagas() {
		return cuotasPagas;
	}


	public void setCuotasPagas(int cuotasPagas) {
		this.cuotasPagas = cuotasPagas;
	}


	public int getCuotasPendientes() {
		return cuotasPendientes;
	}


	public void setCuotasPendientes(int cuotasPendientes) {
		this.cuotasPendientes = cuotasPendientes;
	}


	public float getTotalCobrado() {
		return totalCobrado;
	}


	public void setTotalCobrado(float totalCobrado) {
		this.totalCobrado = totalCobrado;
	}


	public float getTotalPendiente() {
		return totalPendiente;
	}


	public void setTotalPendiente(float totalPendiente) {
		this.totalPendiente = totalPendiente;
	}


	public float getGanancia() {
		return ganancia;
	}


	public void setGanancia(float ganancia) {
		this.ganancia = ganancia;
	}
	
	
	
	

}
